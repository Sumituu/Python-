# Implement matrix addition and subtraction

import numpy as np
matrix1 = np.array([[1, 2],
                   [3, 4]])
matrix2 = np.array([[5, 6],
                   [7, 8]])

addition = matrix1 + matrix2
subtraction = matrix1 - matrix2
print("Matrix Addition Result:")
print(addition)
print("Matrix Subtraction Result:")
print(subtraction)
