# Remove an element from a set
# Create a set
my_set = {1, 2, 3, 4, 5}

# Remove an element from the set
my_set.remove(3)

# Print the updated set
print(my_set)

#OR


# Create a set
my_set1 = {1, 2, 3, 4, 5}

# Remove an element from the set using discard
my_set1.discard(3)

# Print the updated set
print(my_set1)
