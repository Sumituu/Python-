# Convert a list of integers into a set and find the maximum and minimum values.
# Sample list of integers
integer_list = [5, 2, 9, 1, 8, 3, 6]

# Convert the list of integers into a set
integer_set = set(integer_list)

# Find the maximum and minimum values
maximum_value = max(integer_set)
minimum_value = min(integer_set)

# Print the maximum and minimum values
print("Maximum value:", maximum_value)
print("Minimum value:", minimum_value)
